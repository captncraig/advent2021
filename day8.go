package main

import (
	"sort"
)

func day8(input string) (p1Result, p2Result int) {
	lines := Lines(input)
	contest := []int{}
	for _, line := range lines {
		stack := make([]byte, len(line))
		sptr := -1
		score := 0
		for i := 0; i < len(line); i++ {
			switch line[i] {
			case '{':
				sptr++
				stack[sptr] = '{'
			case '<':
				sptr++
				stack[sptr] = '<'
			case '(':
				sptr++
				stack[sptr] = '('
			case '[':
				sptr++
				stack[sptr] = '['
			case '}':
				if stack[sptr] != '{' {
					score = 1197
				}
				sptr--
			case ')':
				if stack[sptr] != '(' {
					score = 3
				}
				sptr--
			case ']':
				if stack[sptr] != '[' {
					score = 57
				}
				sptr--
			case '>':
				if stack[sptr] != '<' {
					score = 25137
				}
				sptr--
			}
			if score != 0 {
				break
			}
		}
		p1Result += score

		if score == 0 {
			newScore := 0
			for sptr >= 0 {
				newScore *= 5
				next := stack[sptr]
				if next == '(' {
					newScore += 1
				}
				if next == '[' {
					newScore += 2
				}
				if next == '{' {
					newScore += 3
				}
				if next == '<' {
					newScore += 4
				}
				sptr--
			}
			contest = append(contest, newScore)
		}
	}
	sort.Ints(contest)
	//log.Println(contest)
	p2Result = contest[len(contest)/2]
	return
}

